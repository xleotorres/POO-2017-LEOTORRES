
public class Exercicio08 {

	public static void main(String[] args) {
		int x = 4;
        
        
        for(int linha = 1 ; linha <= x ; linha++){
        	
            for(int coluna = 1 ; coluna <= linha ; coluna++){
                System.out.print(linha*coluna + " ");
            }
            
            System.out.println();
        	}
	}

}
